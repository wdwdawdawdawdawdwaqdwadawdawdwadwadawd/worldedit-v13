let x = 0
player.onChat("\\\\sphere", function (num1, num2, num3) {
    shapes.sphere(
    blocks.blockWithData(num1, num2),
    builder.position(),
    num3,
    ShapeOperation.Outline
    )
    player.say("§5Made Sphere with a radius of " + num3)
    x = 0
})
player.onItemInteracted(WOODEN_AXE, function () {
    if (x == 0) {
        builder.teleportTo(pos(0, 0, 0))
        builder.mark()
        player.say("§5Set First Position to " + builder.position())
        x = 1
    } else {
        builder.teleportTo(pos(0, 0, 0))
        player.say("§5Set Second Position to " + builder.position())
    }
})
player.onChat("\\\\line", function (num1, num2, num3) {
    builder.line(blocks.blockWithData(num1, num2))
    x = 0
})
player.onChat("\\\\write", function (num1, num2, num3) {
    blocks.print(
    "" + (num3),
    blocks.blockWithData(num1, num2),
    builder.position(),
    EAST
    )
})
player.onChat("\\\\clear", function () {
    x = 0
    player.say("§5Cleared Positions")
})
player.onChat("\\\\set", function (num1, num2) {
    builder.fill(blocks.blockWithData(num1, num2))
    x = 0
})
player.onChat("\\\\paste", function () {
    builder.teleportTo(pos(0, 0, 0))
    builder.paste()
    player.say("§5Clipboard Sucesfully Pasted")
})
player.onChat("\\\\copy", function () {
    builder.copy()
    player.say("§5Selection Copied to Clipboard")
    x = 0
})
player.onChat("\\\\circle", function (num1, num2, num3) {
    shapes.circle(
    blocks.blockWithData(num1, num2),
    builder.position(),
    num3,
    Axis.Y,
    ShapeOperation.Replace
    )
    player.say("§5Made Circle with a radius of " + num3)
    x = 0
})
